    public function sanitizeName($str, $random = null){

        $filtered_name = strtolower(preg_replace('/[^a-zA-Z0-9\.]/', '', $this->remove_accent($str)));
        if (!empty($random)){
            $filtered_name = time() . "_" . $filtered_name;
        }
        return $filtered_name;
