class Utils
{
    public function redirectURL($param)
    {
        header('Location: ' . $param['url']);
        die();
    }

    public function redirectIfNoValidToken()
    {
        $session = new \Intranet\Core\Session();
        $session->getInstance();

        if (empty($session->get('SESSION','token'))){
            $this->redirectURL(array('url' => '/login'));
            die();
        }
    }

    public function randomPassword() {
        return substr(md5(uniqid(mt_rand())), 0, 8);
    }

    public function hashPassword($password) {
        $options = array('cost' => 11);
        return password_hash($password, PASSWORD_BCRYPT, $options);
    }

    public function downloadPDF($url){
        if (file_exists($url)) {
            header('Content-Description: File Transfer');
            header('Content-Type: application/octet-stream');
            header('Content-Disposition: attachment; filename="'.basename($url).'"');
            header('Expires: 0');
            header('Cache-Control: must-revalidate');
            header('Pragma: public');
            header('Content-Length: ' . filesize($url));
            ob_clean();
            flush();
            readfile($url);
            exit;
        }else{
            return false;
        }
    }

